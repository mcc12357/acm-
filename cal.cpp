#include<stdio.h>
double f(double a[],int n,int x)
{
	if(n==0) return a[n];
	int i;
	int tm = 1;
	for(i=1;i<=n;i++) tm = tm * x;
	return a[n] * tm;
}
int main()
{
	while(1)
	{
		double a[7];
		int n,i;
		scanf("%d",&n);
		double ans = 0;
		double x;
		scanf("%lf",&x);
		for(i=0;i<=n;i++) scanf("%lf",&a[i]);
		for(i=0;i<=n;i++) ans += f(a,i,x);
		printf("%.4lf\n",ans);
	}
}
